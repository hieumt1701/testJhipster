    @Test
    @Transactional
    void putNewBook() throws Exception {
        // Initialize the database
        bookRepository.saveAndFlush(book);

        int databaseSizeBeforeUpdate = bookRepository.findAll().size();

        // Update the book
        Book updatedBook = bookRepository.findById(book.getId()).get();
        // Disconnect from session so that the updates on updatedBook are not directly saved in db
        em.detach(updatedBook);
        updatedBook
            .tilte(UPDATED_TILTE)
            .des(UPDATED_DES)
            .image(UPDATED_IMAGE)
            .imageContentType(UPDATED_IMAGE_CONTENT_TYPE)
            .dob(UPDATED_DOB)
            .cv(UPDATED_CV)
            .cvContentType(UPDATED_CV_CONTENT_TYPE);

        restBookMockMvc
            .perform(
                put(ENTITY_API_URL_ID, updatedBook.getId())
                    .contentType(MediaType.APPLICATION_JSON)
                    .content(TestUtil.convertObjectToJsonBytes(updatedBook))
            )
            .andExpect(status().isOk());

        // Validate the Book in the database
        List<Book> bookList = bookRepository.findAll();
        assertThat(bookList).hasSize(databaseSizeBeforeUpdate);
        Book testBook = bookList.get(bookList.size() - 1);
        assertThat(testBook.getTilte()).isEqualTo(UPDATED_TILTE);
        assertThat(testBook.getDes()).isEqualTo(UPDATED_DES);
        assertThat(testBook.getImage()).isEqualTo(UPDATED_IMAGE);
        assertThat(testBook.getImageContentType()).isEqualTo(UPDATED_IMAGE_CONTENT_TYPE);
        assertThat(testBook.getDob()).isEqualTo(UPDATED_DOB);
        assertThat(testBook.getCv()).isEqualTo(UPDATED_CV);
        assertThat(testBook.getCvContentType()).isEqualTo(UPDATED_CV_CONTENT_TYPE);
    }

